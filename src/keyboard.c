unsigned char shift_key = 0;

void kernel_keyboard_init()
{
    kernel_inb(0x64);
}

unsigned char kernel_keyboard_get_raw()
{
    return kernel_inb(0x60);
}

char kernel_keyboard_get_char()
{
    unsigned char data = kernel_keyboard_get_raw();
    char out;

    if (data == 0x2a)
    {
        shift_key = 1;
        return 0;
    }
    if (data == 0xaa)
    {
        shift_key = 0;
        return 0;
    }
    return (shift_key) ? caps[keycodes[data]] : keycodes[data];
}

unsigned char kernel_keyboard_get_arrow()
{
    unsigned char data = kernel_keyboard_get_raw();

    if (data == 0x48)
    {
        return 1;
    }
    if (data == 0x50)
    {
        return 2;
    }
    if (data == 0x4b)
    {
        return 3;
    }
    if (data == 0x4d)
    {
        return 4;
    }

    return 0;
}